import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(scriptDir, '..', '..')
const messagesPath = resolve(repoRoot, 'ToolHub.App', 'Models', 'Messages.cs')
const outputPath = resolve(repoRoot, 'frontend', 'src', 'types', 'bridgeMessageTypes.generated.ts')

function extractConstEntries(source, className) {
  const classPattern = new RegExp(`public static class ${className}\\s*\\{([\\s\\S]*?)\\n\\}`, 'm')
  const body = source.match(classPattern)?.[1]
  if (!body) {
    throw new Error(`Missing ${className} definition in ${messagesPath}`)
  }

  return [...body.matchAll(/public const string (\w+) = "([^"]+)";/g)].map((match) => ({
    name: match[1],
    value: match[2],
  }))
}

function toScreamingSnakeCase(input) {
  return input
    .replace(/([a-z0-9])([A-Z])/g, '$1_$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1_$2')
    .toUpperCase()
}

function renderConstObject(name, entries) {
  const lines = entries.map((entry) => `  ${toScreamingSnakeCase(entry.name)}: '${entry.value}',`)
  return `export const ${name} = {\n${lines.join('\n')}\n} as const`
}

function renderGeneratedFile(messageEntries, logChannelEntries) {
  return `// This file is auto-generated by frontend/scripts/generate-bridge-message-types.mjs.
// Do not edit manually.

type ValueOf<T> = T[keyof T]

${renderConstObject('BRIDGE_MESSAGE_TYPES', messageEntries)}

export type BridgeMessageType = ValueOf<typeof BRIDGE_MESSAGE_TYPES>

${renderConstObject('LOG_CHANNELS', logChannelEntries)}

export type LogChannel = ValueOf<typeof LOG_CHANNELS>
`
}

async function main() {
  const source = await readFile(messagesPath, 'utf8')
  const bridgeMessageEntries = extractConstEntries(source, 'BridgeMessageTypes')
  const logChannelEntries = extractConstEntries(source, 'LogChannels')
  const rendered = renderGeneratedFile(bridgeMessageEntries, logChannelEntries)

  await mkdir(dirname(outputPath), { recursive: true })
  await writeFile(outputPath, rendered, 'utf8')
  console.log(`Generated ${outputPath}`)
}

main().catch((error) => {
  console.error(error)
  process.exitCode = 1
})
